// Função Math.round(parameter) arredonda o número, se for 1.5 ele vira 2.

console.log(Math.round(4.3)) // Saída 4


// Função Math.ceil(parameter) arredonda pro teto(6.2 pra 7)

console.log(Math.ceil(2.1)) // Saída 3


// Função Math.floor(parameter) arredonda pra baixo sempre(6.7 vira 6)

console.log(Math.floor(5.9)) // Saída 5


// Função Math.trunc(parameter) desconsidera números decimais realizando truncamento

console.log(Math.trunc(4.9)) // Saída 4


// Função Math.sqrt(parameter) retorna a raiz quadrada do número

console.log(Math.sqrt(9)) // Saída 3


// Função Math.min(parameter, parameter, parameter) retorna o menor número dentre os argumentos

console.log(Math.min(3, 5, 6, 1.3, 1)) // Saída 1


// Função Math.max(parameter, parameter, parameter) retorna o maior número dentre os argumentos

console.log(Math.max(3, 5, 6, 1.3, 1)) // Saída 6


// Função Math.random() retorna um número randômico de 0 a 1, não possui valor esperado

console.log(Math.random()) // Saída não previsível, exemplo: 0,1323413213 ou 0, 652434132132323